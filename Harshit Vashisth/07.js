// == vs ===
let num1 = "7";
let num2 = 7;
/// == vs ===
console.log(num1 == num2);// == doesn't check datatype only check value that's why it will return true bcz value is same
console.log(num1 === num2);// === check datatype also

//  != vs !==
console.log(num1 != num2);//check value only that's why return false
console.log(num1 !== num2);//check datatype also